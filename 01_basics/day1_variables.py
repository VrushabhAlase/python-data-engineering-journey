# =========================================
# More Variable Examples
# =========================================

# Integer
age = 25
vay = 26

# Float (decimal)
salary = 50000.75
pagar = 44000.50

# String
city = "Pune"
gaw = "Kolhapur"
gender = "Male"

# Boolean
is_working = True
kam_suru_ahe = True

# Print values
print("\n--- Variable Values ---")
print("Age:", age)
print("Salary:", salary)
print("City:", city)
print("Is Working:", is_working)

print("Vay:", vay)
print("Pagar:", pagar)
print("Gaw:", gaw)
print("Kam Suru Ahe:", kam_suru_ahe)
print("Gender:", gender)

# Print types
print("\n--- Variable Types ---")
print(type(age))
print(type(salary))
print(type(city))
print(type(is_working))

print(type(vay))
print(type(pagar))
print(type(gaw))
print(type(kam_suru_ahe))


